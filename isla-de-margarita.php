        <h1>ISLA DE MARGARITA</h1>

        <p>La Isla de Margarita emerge en las cálidas aguas del Mar Caribe al norte de Venezuela, con una superficie de 1.071 Km2, 315 Km de costas y hermosas playas con una temperatura promedio anual de 27ºC y que conforma, junto,a Coche y Cubagua, el Estado Nueva Esparta. Es un conjunto de tres islas que tienen la misma constitución geológica. Ubicada en la región caribeña, al norte de Venezuela, a 38 Kms de la costa nororiental.</p>

        <ul>
            <li>Población: Estimada en 350.000 habitantes según el censo nacional del año 2011</li>
            <li>Capital: La Asunción</li>
            <li>Principal actividad  económica: Turismo, Puerto Libre, Pesca.</li>
            <li>Clima: 30º C promedio durante el año. Temperatura del agua 27º C promedio. Humedad 70%. Lluvia 22 pulgadas anuales. Fuera del área de Huracanes.</li>
        </ul>

        <h2>PLAYAS</h2>
        <p>Playas caribeñas de cristalinas aguas mansas donde el viento sopla todo el año como Playa Moreno, El Silguero, Pampatar y El Yaque, esta última reconocida mundialmente para la práctica del windsurf, hacen de la Isla de Margarita una fuente avasallante de energía. Playas oceánicas como El Agua, Parguito y Guacuco, con abundante oleaje y la más blanca espuma del mar, donde el visitante encontrará todos los servicios de hoteles y restaurantes, además de poder practicar el surf. Otras playas como Manzanillo, Pto. Cruz, La Restinga y Juan Griego, por mencionar algunas playas semi-oceánicas de palpitante mar, donde brillan sin cesar todos los deportes playeros, así como la pesca artesanal. Isla de Coche con playas de arena blanca, pintorescas cabañas y todos los deportes náuticos a la orilla del mar.</p>

        <h2>WINSURF Y KITESURF</h2>
        <p>Windsurf  y Kitesurf en la Isla Margarita se asocia directamente con Playa "El Yaque", el único lugar en el mundo que garantiza unas condiciones ideales para la práctica de esta disciplina durante cualquier época del año. El Yaque es conocido mundialmente por sus eventos nacionales e internacionales como la regata “Wild Wind” que se realiza durante el mes de junio agrupando participantes de todas las latitudes del mundo que se dan cita año tras año acrecentando el apogeo de este magnífico escenario en deportes náuticos.</p>

        <p>La posición geográfica de “El Yaque” permite que los vientos con dirección este – oeste se deslicen paralelamente a la costa, obteniendo una aceleración natural debido al efecto térmico de sus aguas de sólo 1,5 metros de profundidad en sus primeros 300 metros de longitud combinados con el efecto que produce una cadena montañosa localizada al norte la cual crea un circuito de aceleración con vientos promedios de fuerza 4 Bft (en escala de Beaufort). Este es el gran secreto, que permite que El Yaque, pueda garantizar al visitante un viento seguro durante todo el año y una de las causas que la han hecho tan famosa en todo el mundo.</p>

        <p>Durante el mes de Enero la llegada de los vientos alisios convierten a Margarita en la Formula Uno del Windsurf. Vientos constantes con fuerza 5, 6, y 7 Bft son comunes durante este periodo (Enero-Junio). La calidez de sus aguas complementan las condiciones óptimas de este lugar manteniéndose en un rango de 27°C a 32°C durante el año.</p>

        <p>En conclusión El Yaque reúne condiciones inmejorables para cualquiera de las disciplinas. Tablas del tipo olímpicas, slalom y de olas son utilizadas por novatos y expertos, jóvenes y no tan jóvenes. No en vano las más famosas escuelas y clubes de windsurf del mundo se encuentran representadas en Margarita. </p>

        <p>El Yaque también ofrece una completa infraestructura de hotelería en la zona. Esto permite al turista escoger diversas opciones que pueden incluir hasta los paquetes de windsurf, además de combinar estadías con sus sucursales dando la oportunidad de disfrutar otras locaciones como Coche y Los Roques, esta última de impactante belleza natural.</p>

        <h2>BUCEO</h2>

        <p>La Isla de Margarita ofrece aguas cálidas y cristalinas y con una diversidad de ambientes submarinos que ofrece la oportunidad de practicar actividades submarinas, tales como el buceo y el snorkeling. Junto con sus islas y archipiélagos vecinos hacen de este viaje, sus vacaciones para recordar. Sitios como Los Frailes, con su grupo de pequeños islotes que ofrecen piscinas naturales transparentes para realizar snorkeling localizada al noroeste de la isla, o El Farallón, de formación rocosa localizada a 3 millas de la bahía de Pampatar, son nada más el preámbulo para los impresionantes escenarios que encontrarán en lugares como el Archipiélago de los Roques, Los Testigos y la Isla de la Blanquilla.</p>

        <p>Estas zonas reciben muy poca lluvia durante el año lo que las hace muy bajas en contenido de cieno haciendo sus aguas con visibilidad de hasta de 30 metros (100 pies) con una temperatura de 25°C promedio. Margarita cuenta además con una confiable infraestructura a nivel de centros de buceo debidamente autorizados por las organizaciones internacionales que podrán organizar en sus excursiones para pequeños y grandes grupos, tales como SSI (Scuba Schools International) y PADI (Professional Association of Diving Instructors).</p>

        <h2>COMPRAS</h2>

        <p>Margarita puede ser definida como el centro comercial al cielo abierto más grande y variado del Caribe. Para muchas personas, Margarita significa "compras". Y el motivo es muy sencillo: Margarita es Puerto Libre, es decir libre de impuestos. Por esto, abundan los centros comerciales, en donde se pueden comprar artículos importados, de las más diversas marcas y calidades. Los que buscan buenos precios, van hacia el centro de Porlamar (Calle Guevara y adyacentes), Av. 4 de Mayo, Av. Santiago Mariño o al mercado de Conejeros. Los que buscan un mejor ambiente de compras van a los diversos centros comerciales, los cuales en temporada alta, se encuentran abiertos hasta las 11:00 PM.  Más de 2000 opciones de tiendas compiten con artículos de alta calidad importados de todas partes del mundo. Cualquier producto que busque lo hallará aquí y a precios de "Puerto Libre".</p>

        <p>Los principales sectores comerciales están en los Bulevares Gómez y Guevara y las Avenidas Santiago Mariño, 4 de Mayo de Porlamar y en la Calle Marina de Juangriego.</p>

        <h2>ALOJAMIENTO</h2>

        <p>La Isla de Margarita tiene hospedaje, hoteles, resorts y posadas. La Isla de Margarita emerge como un destino turístico que posee una oferta hotelera a la par de las más capacitadas del Caribe. De esta forma, el turista tiene la posibilidad de escoger entre un imponente hotel de 5 estrellas o decidirse por una acogedora posada. Esta infraestructura hace de La Isla de Margarita una de las mejores locaciones turísticas de la región del Mar Caribe.</p>
        
